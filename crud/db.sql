CREATE database if not exists cadastro char set utf8;
USE cadastro;
CREATE table pessoa(
    id_pessoa int  primary key auto_increment,
    nome varchar(255),
    tel varchar(25), 
    email varchar(255),
    nasc varchar(255),
    ec varchar(255),
    age varchar(3),
    area varchar(255),
    andress varchar(255),
    formacao varchar(255),
    cnc varchar(255),
    expe varchar(255),
    obj varchar(255)
);











